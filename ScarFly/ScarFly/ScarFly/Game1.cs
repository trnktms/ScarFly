            graphics.IsFullScreen = true;

            List<MenuButton> buttons = new List<MenuButton>();
            buttons.Add(new MenuButton("Start","testButton", 10, 10));
            buttons.Add(new MenuButton("Scores", "testButton", 400, 200));

            mainMenu = new MainMenu(buttons);
        }

